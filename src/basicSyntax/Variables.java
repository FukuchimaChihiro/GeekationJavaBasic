package basicSyntax;

public class Variables {

	public static void main(String[] args) {
		int num1 = 5;
		double num2 = 2.5;
		String like = "私の好きな";
		String z = "です。";
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(like + "整数は" + num1 + z);
		System.out.println(like + "少数は" + num2 + z);
	}

}
